    /***
     * A function that returns either a random date between two dates or just a random date
     * partial source: https://gist.github.com/samcrosoft/6550473
     *
     * @param \DateTime|NULL $start_date
     * @param \DateTime|NULL $end_date
     * @return \DateTime
     */
    public static function returnRandomDate(\DateTime $start_date = NULL, \DateTime $end_date = NULL) : \DateTime {

        if(isset($start_date, $end_date)) {

            $min = strtotime($start_date->format("Y-m-d H:i:s"));
            $max = strtotime($end_date->format("Y-m-d H:i:s"));
            $random = mt_rand($min, $max);

            return (new \DateTime())->setTimestamp($random);

        } else {

            $timestamp = mt_rand(1, time());
            return (new \DateTime())->setTimestamp($timestamp);
        }
    }
